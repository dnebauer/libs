=head1 NAME

Role::Utils::Dn::ConfigFile - returned by Role::Utils::Dn->config_file()

=head1 SYNOPSIS

    with qw(Role::Utils::Dn);

    my $stem = 'my_config';
    my $cfg = $self->config_file($stem);

    # retrieve all config file data as a hashref
    my $all_conf_data = $cfg->data;

    # retrieve a single config file value
    my $conf_value = $cfg->get_value($my_key, $my_section);

=head1 VERSION

This documentation refers to Role::Utils::Dn::CommandResult version 0.8.

=head1 DESCRIPTION

Captures results of running a command with the C<Role::Utils::Dn> method
C<config_file>.

=head2 Selecting configuration file

The resulting object makes available configuration file data.
The configuration file used is based on a provided "stem".

The steps followed to select a configuration file are:

=over

=item

The module L<File::ConfigDir> generates a list of possible configuration
directories.

=item

A list of file suffixes, such as F<rc> and F<.ini>, is obtained from the
calling script or, if none are provided, a default list.

=item

For every combination of configuration directory and file suffix, the
following are added to a list of candidate configuration filepaths:

=over

=item

F<$dir/$stem$suffix>

=item

F<$dir/$stem/$stem$suffix>

=back

=item

The first candidate filepath which is found to exist is used.

=back

=head2 Reading the configuration file

The module L<Config::Any> is used to extract data from the chosen
configuration file. This module, in turn, uses other modules to read the
configuration file depending on the file format, for example,
L<JSON::MaybeXS> for json data or L<Config::Tiny> for ini data.

A quirk of L<Config::Any>'s operation means that the configuration file's
extension is used as a short-cut to determine data format and, in turn,
the loader module to use. For example, if a file has a F<.json> extension
the L<JSON::MaybeXS> loader module is used.

This means a non-specific file extension like F<.cfg> will cause a fatal
"no loaders available" error.

While more extensive testing is required to confirm this, it appears that
file suffixes like F<rc>, F<.rc>, F<.cnf> and F<.conf> are considered to
be formatted like Apache configuration files. This format is documented in
numerous locations, including the
L<Apache http server project|https://httpd.apache.org/docs/current/configuring.html>.

B<Warning:> When reading the configuration file a mismatch between the
inferred file format and the actual data format may not be detected. The
file may instead be incorrectly parsed and erroneous data returned by the
objects's C<data> attribute, while the C<get_value> method may return an
incorrect key value of mistakenly report that there is no matching key in
the file.

=head1 SUBROUTINES/METHODS

=head2 get_value($key[, $section])

=head3 Purpose

Gets a single configuration file value.

=head3 Parameters

=over

=item $key

Name of configuration file key. Scalar string. Required.

B<Warning:> Key names are case sensitive.

B<Warning:> A fatal error occurs if the key is not found.

=item $section

Name of configuration file section. Scalar string. Optional.
Default: 'default'.

B<Warning:> A fatal error occurs if the section is not found.
This includes the "default" section if no section name if provided.

B<Warning:> Section names are case sensitive.

=back

=head3 Prints

Error messages on failure.

=head3 Returns

Configuration file value. Scalar or reference, depending on data configuration.

=head1 CONFIGURATION AND ENVIRONMENT

=head2 Properties

The C<stem>, C<silent> and C<suffixes> attributes are provided at startup.

The C<data> attribute is generated at object instantiation and is available
to the calling script.

=head3 stem

Configuration file stem provided at object construction. Scalar string.

=head3 silent

Whether to suppress informational messages about which configuration files
were located and which one was selected, provided at object construction.
Scalar boolean. Optional. Default: false.

=head3 suffixes

File extensions or endings that the configuration file may have, provided at
object construction. Arrayref. Optional.

Default arrayref contents:

=over

=item

F<rc>

=item

F<.rc>

=item

F<.cnf>

=item

F<.conf>

=item

F<.ini>

=item

F<.json>

=item

F<.jsn>

=item

F<.xml>

=item

F<.yaml>

=item

F<.yml>

=back

=head3 data

The extracted configuration data. Hashref.

=head2 Configuration files

None used.

=head2 Environment variables

None used.

=head1 DIAGNOSTICS

Unless otherwise noted, all messages documented here are fatal.

=head2 Expected array with 1 element, got NUM

=head2 Expected hashref, got: scalar

=head2 Expected HASH reference, got: VAR_TYPE

These errors occur when the configuration file is being read and the data
is in an unexpected format.

=head2 No config section named NAME

The C<get_value> method gives this error if it cannot locate the
specified section in the configuration file data.

=head2 No configuration files located

Occurs if none of the combinations of candidate configuration directories,
file stem, and suffixes result in a locatable filepath.

Troubleshooting steps include checking that the:

=over

=item file stem is spelled correctly

=item configuration file is in a standard configuration directory

=item file suffix is included in the list of checked suffixes.

=back

=head2 No key named NAME in section NAME

The C<get_value> method gives this error if it cannot locate the
specified section/key in the configuration file data.

=head2 No stem name provided

Occurs if no file stem provided at object construction.

=head2 There are no loaders available for .EXT files

As noted in the L</DESCRIPTION> section, to read the configuration file
the L<Config::Any> module infers the configuration file's data format from
it's file extension and selects an appropriate module to use as a loader.
This error message occurs if L<Config::Any> is unable to match a loader
module to the file extension. See the L<Config::Any> documentation for
more details.

=head2 Unable to generate list of candidate configuration directories

Occurs if L<File::ConfigDir> fails to generate any candidate configuration
directories.

