package Role::Utils::Dn::ConfigFile;

# modules/constants    {{{1
use Moo;
use strictures 2;
use 5.038_001;
use version; our $VERSION = qv('0.8');
use namespace::clean;

use Carp;
use Config::Any;
use Const::Fast;
use File::ConfigDir;
use MooX::HandlesVia;
use Types::Path::Tiny;

const my $TRUE                   => 1;
const my $FALSE                  => 0;
const my $MSG_SCALAR_NOT_HASHREF => 'Expected hashref, got: scalar';
const my $REF_TYPE_HASH          => 'HASH';                            # }}}1

# attributes

# stem    {{{1
has 'stem' => (
  is       => 'ro',
  isa      => Types::Standard::Str,
  required => $TRUE,
  doc      => 'Basename of configuration file',
);

# suffixes, _suffixes    {{{1
has 'suffixes' => (
  is       => 'ro',
  isa      => Types::Standard::ArrayRef [Types::Standard::Str],
  required => $FALSE,
  default  => sub {
    return [
      'rc',    '.rc',  '.cnf', '.conf', '.ini',
      '.json', '.jsn', '.xml', '.yaml', '.yml',
    ];
  },
  handles_via => 'Array',
  handles     => { _suffixes => 'elements' },
  doc         => 'Possible suffixes for config file name',
);

# silent    {{{1
has 'silent' => (
  is       => 'ro',
  isa      => Types::Standard::Bool,
  required => $FALSE,
  default  => $FALSE,
  doc      => 'Whether to provide non-error feedback',
);

# _conf_fp    {{{1
has '_conf_fp' => (
  is     => 'lazy',
  isa    => Types::Path::Tiny::AbsPath,
  coerce => Types::Path::Tiny::AbsPath->coercion,
  doc    => 'Path of configuration file (must exist)',
);

sub _build__conf_fp($self) {    ## no critic (Subroutines::ProhibitUnusedPrivateSubroutines)
  my $stem = $self->stem;
  croak 'No stem name provided' if not $stem;
  my @candidate_dirs = File::ConfigDir::config_dirs();
  croak 'Unable to generate list of candidate configuration directories'
      if not @candidate_dirs;
  my @suffixes = $self->_suffixes;

  # generate list of candidate config filepaths
  my @candidate_fps;
  for my $dir (@candidate_dirs) {
    for my $suffix (@suffixes) {
      push @candidate_fps, "$dir/$stem$suffix", "$dir/$stem/$stem$suffix";
    }
  }

  # get config data from any found candidate filepaths
  # • Config::Any returns a data structure like:
  #     [
  #       { '/home/david/rtng.json' => { $config_data_hashref } },
  #       { '/home/david/.config/rtng.ini' => { $config_data_hashref } }
  #     ]
  my $conf_matches =
      Config::Any->load_files(
    { files => [@candidate_fps], use_ext => $TRUE });
  croak 'No configuration files located' if not @{$conf_matches};

  # extract filepaths
  my @config_fps = map { (keys %{$_})[0] } @{$conf_matches};

  # use first match and report on extra matches
  my $config_fp = shift @config_fps;
  if (not $self->silent) {
    say "Using configuration file: $config_fp" or croak;
  }
  if (@config_fps and not $self->silent) {
    say "Multiple configuration files located based on stem '$stem'" or croak;
    say 'Ignoring:'                                                  or croak;
    for my $fp (@config_fps) {
      say "- $fp" or croak;
    }
  }

  return $config_fp;
}

# data    {{{1
has 'data' => (
  is  => 'lazy',
  isa => Types::Standard::HashRef,
  doc => 'Configuration data returned by Config::Any',
);

sub _build_data($self) {    ## no critic (Subroutines::ProhibitUnusedPrivateSubroutines)

  # used for checking variable type
  my $var_type;

  # assume config file exists
  my $conf_fp = $self->_conf_fp;

  # • need 'use_ext => $TRUE'
  #   - if omitted get extraneous warning:
  #       use_ext argument was not explicitly set, as of 0.09, \
  #       this is true by default at /usr/share/perl5/Config/Any.pm \
  #       line 137.
  #   - if set to $FALSE, aberrant quoting destroys the data
  #   - note the following warning about use of 'use_ext':
  #       If the "use_ext" parameter is defined, the loader will attempt
  #       to parse the file extension from each filename and will skip
  #       the file unless it matches a standard extension for the
  #       loading plugins. Only plugins whose standard extensions match
  #       the file extension will be used. For efficiency reasons, its
  #       use is encouraged, but be aware that you will lose flexibility
  #       -- for example, a file called "myapp.cfg" containing YAML data
  #       will not be offered to the YAML plugin, whereas "myapp.yml" or
  #       "myapp.yaml" would be.
  #   - the error generated by a non-standard extension like 'cfg' is:
  #       There are no loaders available for .cfg files at \
  #       /home/david/tmp/dncommon/test.pl line 137.
  #   - a mismatch between loader (selected based on file extension) and
  #     internal file format may not generate an error when loading config
  #     file data, but it may misinterpret it, leading either to a failure
  #     to find a section/key value or to returning an incorrect value
  my $loaded_data =
      Config::Any->load_files({ files => [$conf_fp], use_ext => $TRUE });

  # expect to get data structure like: [ { 'conf_fp' => { <conf_data> } } ]
  # • get enclosed hashref
  my $length = scalar @{$loaded_data};
  croak "Expected array with 1 element, got ${length}" if $length != 1;
  my @loaded_data_list = @{$loaded_data};
  my $fp_data_hashref  = $loaded_data_list[0];
  $var_type = ref $fp_data_hashref;
  croak $MSG_SCALAR_NOT_HASHREF if $var_type eq q{};
  croak "Expected HASH reference, got: $var_type"
      if $var_type ne $REF_TYPE_HASH;

  # • get config data (the hashref value)
  my %fp_data_hash        = %{$fp_data_hashref};
  my @fp_data_hash_values = values %fp_data_hash;
  my $conf_data           = $fp_data_hash_values[0];
  $var_type = ref $conf_data;
  croak $MSG_SCALAR_NOT_HASHREF if $var_type eq q{};
  croak "Expected HASH reference, got: $var_type"
      if $var_type ne $REF_TYPE_HASH;

  return $conf_data;
}    # }}}1

# methods

# get_value($key, [$section])    {{{1
#
# does:   get configuration file value for given key
# params: key     - config key [string, required]
#         section - config file section
#                   [string, optional, default='default']
# prints: feedback on error
# return: scalar arrayref, dies on failure
sub get_value ($self, $key, $section = 'default') {

  # get section data
  my $conf_data_ref = $self->data;
  my %conf_data     = %{$conf_data_ref};
  croak "No config section named '$section'"
      if not exists $conf_data{$section};
  croak "No config section named '$section'"
      if not defined $conf_data{$section};
  my $sect_data_ref  = $conf_data{$section};
  my $sect_data_type = ref $sect_data_ref;
  croak $MSG_SCALAR_NOT_HASHREF if $sect_data_type eq q{};
  croak "Expected HASH reference, got: $sect_data_type"
      if $sect_data_type ne $REF_TYPE_HASH;
  my %sect_data = %{$sect_data_ref};

  # get key value within section
  croak "No key named '$key' in section '$section'"
      if not exists $sect_data{$key};
  croak "No key named '$key' in section '$section'"
      if not defined $sect_data{$key};
  my $value = $sect_data{$key};

  return $value;
}    # }}}1

